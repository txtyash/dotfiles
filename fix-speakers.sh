#!/usr/bin/env bash
# fix-speakers.sh

i2c_bus=0
addresses=(0x3d 0x38)

for i in "${!addresses[@]}"; do
  addr=${addresses[$i]}
  
  write_regs() {
    local dev=$1
    shift
    while [ $# -gt 0 ]; do
      i2cset -f -y "$i2c_bus" "$dev" "$1" "$2"
      shift 2
    done
  }

  # Initialization
  write_regs "$addr" 0x00 0x00 0x7f 0x00 0x01 0x01 0x0e 0xc4 0x0f 0x40 0x5c 0xd9 0x60 0x10

  if [ "$i" -eq 0 ]; then
    i2cset -f -y "$i2c_bus" "$addr" 0x0a 0x1e
  else
    i2cset -f -y "$i2c_bus" "$addr" 0x0a 0x2e
  fi

  # Remainder
  write_regs "$addr" \
    0x0d 0x01 0x16 0x40 0x00 0x01 0x17 0xc8 0x00 0x04 \
    0x30 0x00 0x31 0x00 0x32 0x00 0x33 0x01 0x00 0x08 \
    0x18 0x00 0x19 0x00 0x1a 0x00 0x1b 0x00 0x28 0x40 \
    0x29 0x00 0x2a 0x00 0x2b 0x00 0x00 0x0a 0x48 0x00 \
    0x49 0x00 0x4a 0x00 0x4b 0x00 0x58 0x40 0x59 0x00 \
    0x5a 0x00 0x5b 0x00 0x00 0x00 0x02 0x00
done
